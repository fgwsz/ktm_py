from keycode import KeyCode

# 快捷键设置
class KeyCodeOf:
    leader                 =KeyCode._LeftAlt
    mouse_move_up          =KeyCode._K      # Vim K
    mouse_move_down        =KeyCode._J      # Vim J
    mouse_move_left        =KeyCode._H      # Vim H
    mouse_move_right       =KeyCode._L      # Vim L
    mouse_wheel_up         =KeyCode._B      # Back
    mouse_wheel_down       =KeyCode._N      # Next
    mouse_left_click       =KeyCode._O      # GameBoy O
    mouse_left_double_click=KeyCode._A      # GameBoy A
    mouse_right_click      =KeyCode._X      # GameBoy X
    mouse_key_up           =KeyCode._U      # Up or Undo
    mouse_left_down        =KeyCode._G      # Get
    mouse_right_down       =KeyCode._I      # Invert
    mouse_dpixel_double    =KeyCode._2      # * 2
    mouse_dpixel_halve     =KeyCode._Hyphen # - Decrease
    app_quit               =KeyCode._Q      # Quit